     void Refresh_itens() throws SQLException, ClassNotFoundException{
        ClienteDAO clienteDAO = new ClienteDAO();
        caminhaoDAO caminhaoDAO = new caminhaoDAO();
        VendaDAO vendaDAO = new VendaDAO();
         
        CodCaminhao_TF.setText(String.valueOf(caminhaoDAO.MaiorID()+1));
        CodfVenda_TF.setText(String.valueOf(vendaDAO.MaiorID()+1));
        
        
        ArrayList <Cliente> ListaClientes = clienteDAO.getAll();
        
        DefaultTableModel model;
        model = new DefaultTableModel(new String[] {"cpf", "nome", "endereco", "email", "telefone"}, 0);
        
        ListaClientes.forEach((Cliente cliente) -> {
                        String cpf = cliente.getCpf();
                        String nome = cliente.getNome();
                        String endereco = cliente.getEndereco();
                        String email = cliente.getEmail();
                        String telefone = cliente.getTelefone();
                        
                        Vector row = new Vector();
                        row.add(cpf);
                        row.add(nome);
                        row.add(endereco);
                        row.add(email);
                        row.add(telefone);
                        model.addRow(row);
                    });

                    Tabela_Clientes.setModel(model);
                    
        ArrayList <Caminhao> ListaCaminhoes = caminhaoDAO.getAll();
        
        DefaultTableModel model2;
        model2 = new DefaultTableModel(new String[] {"CodCaminhao", "Potencia", "Modelo"}, 0);
        
        ListaCaminhoes.forEach((Caminhao caminhao) -> {
                        int CodCaminhao = caminhao.getCodCaminhao();
                        String Modelo = caminhao.getModelo();
                        float Potencia = caminhao.getPotencia();
                        
                        
                        Vector row = new Vector();
                        row.add(CodCaminhao);
                        row.add(Potencia);
                        row.add(Modelo);
                        model2.addRow(row);
                    });

                    Tabela_Caminhoes.setModel(model2);
                    
        ArrayList <Venda> ListaVendas = vendaDAO.getAll();
        
        DefaultTableModel model3;
        model3 = new DefaultTableModel(new String[] {"CodVenda", "FK_CPF", "FK_CodCaminhao", "Data", "valor"}, 0);
        
        ListaVendas.forEach((Venda venda) -> {
                        int CodVenda = venda.getCodVenda();
                        String FK_CPF = venda.getFK_CPF();
                        int FK_CodCaminhao = venda.getFK_CodCaminhao();
                        LocalDate Data = venda.getData();
                        float valor = venda.getValor();
                        
                        Vector row = new Vector();
                        row.add(CodVenda);
                        row.add(FK_CPF);
                        row.add(FK_CodCaminhao);
                        row.add(Data);
                        row.add(valor);
                        model3.addRow(row);
                    });

                    Tabela_Vendas.setModel(model3);            
    }
    
